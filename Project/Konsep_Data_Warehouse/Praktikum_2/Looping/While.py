# Inisialisasi varibel
hitung = 0
# membuat perintah while loop
# looping akan berhenti jika kondisi true (hitung = 9)
print("While Looping : ")
while (hitung < 9):
    # eksekusi hasil
    print("Berhitung : ", hitung)
    # membuat increment untuk mengubah variabel hingga memmenuhi kondisi true
    hitung = hitung + 1

print("Selesai !")
