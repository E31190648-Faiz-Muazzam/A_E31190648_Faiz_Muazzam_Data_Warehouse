print("======================Pernyataan If Else============================")
# percabangan if else dieksekusi berdasarkan kondisi true dan false,
# namun apabila kondisi true program akan menjalankan statement satu
# dan apabila kondisi false maka program akan menjalankan statement dua.
# inisial variabel
nilai = 9 #menentukan kondisi
if (nilai < 7):
    # statement jika true
    print("Kemampuan Anda Kurang")
else:
    # statement jika false
    print("Kemampuan Anda Sudah Melampaui Batas")
